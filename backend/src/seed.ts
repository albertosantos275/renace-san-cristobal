async function main() {
  console.log('🌱 Iniciando seed de base de datos...')

  // Config defaults
  const configs = [
    { clave: 'meta_ciudadana',            valor: '50000' },
    { clave: 'slogan',                    valor: 'Construyamos juntos el San Cristóbal que merecemos.' },
    { clave: 'registro_publico_activo',   valor: 'true' },
    { clave: 'texto_contador',            valor: 'CIUDADANOS REGISTRADOS' },
  ]
  for (const c of configs) {
    await prisma.config.upsert({ where: { clave: c.clave }, update: {}, create: c })
  }

  // Sectors with real polygon coordinates
  for (const s of SECTOR_GEO_DATA) {
    const { lat, lng } = centroid(s.coords)
    await prisma.sector.upsert({
      where: { nombre: s.nombre },
      update: { zone: s.zone, centerLat: lat, centerLng: lng, polygon: poly(s.coords) },
      create: { nombre: s.nombre, zone: s.zone, centerLat: lat, centerLng: lng, polygon: poly(s.coords) },
    })
  }

  // Admin user
  const adminHash = await bcrypt.hash('admin2028', 10)
  await prisma.user.upsert({
    where: { email: 'admin@renace2028.do' },
    update: {},
    create: { nombre: 'Administrador', email: 'admin@renace2028.do', passwordHash: adminHash, rol: 'ADMIN' }
  })

  // Promoter users
  const promotores = [
    { nombre: 'Luis Martínez',  email: 'luis@renace2028.do',  sectores: ['Madre Vieja', 'Pueblo Nuevo'] },
    { nombre: 'María García',   email: 'maria@renace2028.do', sectores: ['Lava Pies'] },
    { nombre: 'Juan Pérez',     email: 'juan@renace2028.do',  sectores: ['Canastica', 'El Cerro', 'Moscú'] },
  ]
  const promoterHash = await bcrypt.hash('promotor2028', 10)
  const promotorIds: number[] = []

  for (const p of promotores) {
    const user = await prisma.user.upsert({
      where: { email: p.email },
      update: { sectores: JSON.stringify(p.sectores) },
      create: { nombre: p.nombre, email: p.email, sectores: JSON.stringify(p.sectores), passwordHash: promoterHash, rol: 'PROMOTER' }
    })
    promotorIds.push(user.id)
  }

  // Sample citizens (only if none exist AND demo data is explicitly enabled).
  // In production we leave this off so the real counter starts at 0.
  const existingCount = await prisma.citizen.count()
  if (process.env.SEED_DEMO === 'true' && existingCount === 0) {
    const allSectors = await prisma.sector.findMany()
    const prioOpciones = ['Empleo', 'Limpieza de la ciudad', 'Seguridad ciudadana', 'Agua potable', 'Arreglo de calles', 'Juventud']
    const niveles = ['NEUTRAL', 'SIMPATIZANTE', 'APOYA', 'MUY_COMPROMETIDO', 'LIDER_COMUNITARIO']
    const problemas = [
      'Necesitamos más agua en el barrio.',
      'Los jóvenes necesitan más oportunidades.',
      'Las calles están en muy mal estado.',
      'Falta iluminación en las noches.',
      'Necesitamos más seguridad.',
      'El transporte público es muy malo.',
      'Los niños no tienen espacios de recreación.',
    ]

    const volunteersByPromoter: Record<number, number[]> = {}
    const nonVolunteers: { id: number; promotorId: number; sector: string }[] = []

    for (let i = 0; i < 150; i++) {
      const sector = allSectors[Math.floor(Math.random() * allSectors.length)]
      const promotorId = promotorIds[Math.floor(Math.random() * promotorIds.length)]
      const nivel = niveles[Math.floor(Math.random() * niveles.length)]
      const voluntario = Math.random() > 0.7
      const whatsapp = Math.random() > 0.4
      const prios = prioOpciones.sort(() => 0.5 - Math.random()).slice(0, Math.floor(Math.random() * 3) + 1)
      const problema = Math.random() > 0.4 ? problemas[Math.floor(Math.random() * problemas.length)] : null

      let score = 0
      if (voluntario) score += 20
      if (nivel === 'SIMPATIZANTE') score += 15
      if (nivel === 'APOYA') score += 25
      if (nivel === 'MUY_COMPROMETIDO') score += 30
      if (nivel === 'LIDER_COMUNITARIO') score += 40
      if (whatsapp) score += 10
      if (problema) score += 10
      score = Math.min(score, 100)

      const daysAgo = Math.floor(Math.random() * 60)
      const createdAt = new Date(); createdAt.setDate(createdAt.getDate() - daysAgo)

      try {
        const citizen = await prisma.citizen.create({
          data: {
            nombreCompleto:     `Ciudadano Demo ${i + 1}`,
            cedula:             `00${String(i + 1).padStart(11, '0')}`.slice(0, 13),
            telefono:           `809${String(Math.floor(Math.random() * 9000000) + 1000000)}`,
            edad:               18 + Math.floor(Math.random() * 50),
            sexo:               Math.random() > 0.5 ? 'M' : 'F',
            sector:             sector.nombre,
            sectorGeoId:        sector.id,
            votaEnSanCristobal: Math.random() > 0.1,
            whatsappUpdates:    whatsapp,
            voluntario,
            nivelApoyo:         nivel,
            prioridades:        JSON.stringify(prios),
            problemaComunidad:  problema,
            scorePolit:         score,
            registradoPorId:    promotorId,
            createdAt,
          }
        })

        if (sector.centerLat && sector.centerLng) {
          const jitter = () => (Math.random() - 0.5) * 0.005
          await prisma.citizenLocation.create({
            data: { citizenId: citizen.id, sectorId: sector.id, latitude: sector.centerLat + jitter(), longitude: sector.centerLng + jitter(), locationSource: 'manual_sector' }
          })
        }

        await prisma.registrationEvent.create({
          data: { citizenId: citizen.id, promoterId: promotorId, sectorId: sector.id, registrationMethod: 'promoter_app', createdAt }
        })

        if (voluntario) (volunteersByPromoter[promotorId] ||= []).push(citizen.id)
        else nonVolunteers.push({ id: citizen.id, promotorId, sector: sector.nombre })
      } catch {
        // skip duplicates
      }
    }

    // Linaje demo: ~55% de los no-voluntarios fueron referidos por un voluntario del mismo promotor
    for (const nv of nonVolunteers) {
      const vols = volunteersByPromoter[nv.promotorId]
      if (vols && vols.length && Math.random() > 0.45) {
        const referidoPorId = vols[Math.floor(Math.random() * vols.length)]
        await prisma.citizen.update({ where: { id: nv.id }, data: { referidoPorId } })
      }
    }
  }

  console.log('✅ Seed completado!')
  console.log('\n📋 Credenciales:')
  console.log('  ADMIN:    admin@renace2028.do / admin2028')
  console.log('  PROMOTOR: luis@renace2028.do / promotor2028')
  console.log(`  Sectores: ${SECTOR_GEO_DATA.length} sectores con polígonos reales`)
}

main().catch(console.error).finally(() => prisma.$disconnect())
